int		is_anagram(char *a, char *b)
{
	int	ret;

	ret = 0;
	while (*a != '\0' && *b != '\0')
	{
		ret += *a;
		ret -= *b;
		a += 1;
		b += 1;
	}
	return (ret == 0 ? 1 : 0);
}
